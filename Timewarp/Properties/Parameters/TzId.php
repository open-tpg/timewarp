namespace TPG\Timewarp\Properties\Parameters;


class TzId extends Parameter
{
    protected $name = 'TZID';

    /**
     * TzId constructor.
     * @param string $value
     */
    public function __construct(string $value)
    {
        $this->value = $value;
    }

    public function toString(): string
    {
        return $this->value;
    }
}
